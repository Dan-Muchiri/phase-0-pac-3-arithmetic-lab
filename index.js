function add(a,b){
    let c = a + b;
    return(c);
}
function subtract(a,b){
    let c = a - b;
    return(c);
}
function multiply(a,b){
    let c = a * b;
    return(c);
}
function divide(a,b){
    let c = a / b;
    return(c);
}
function increment(n){
    let c = ++n;
    return(c);
}
function decrement(n){
    n--;
    let c = n;
    return(c);
}
function makeInt(n){
    let c = parseInt(n,10);
    return(c);
}
function preserveDecimal(n){
    let c = parseFloat(n);
    return(c);
}